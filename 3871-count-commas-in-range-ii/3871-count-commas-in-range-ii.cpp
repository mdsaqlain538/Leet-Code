class Solution {
public:
    long long countCommas(long long n) {

        if(n<999){
            return 0;
        }

        if(n>=1000 && n<=999999){
            return abs((n-999));
        }

        if(n>=1000000 && n<=999999999){
            return abs((n-999999)*2) + abs((999999-999));
        }

        if(n>=1000000000 && n<=999999999999){
            return abs((n-999999999)*3) + abs((999999999-999999)*2) + abs((999999-999));
        }

        if(n>=1000000000000 && n<=999999999999999){
            return abs((n-999999999999)*4) + abs((999999999999-999999999)*3) + abs((999999999-999999)*2) + abs((999999-999));
        }

        if(n>=1000000000000000 && n<=999999999999999999){
            return ((n-999999999999999)*5) + abs((999999999999999-999999999999)*4) + abs((999999999999-999999999)*3) + abs((999999999-999999)*2) + abs((999999-999));;
        }

        return {};
    }
};